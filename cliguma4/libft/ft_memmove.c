#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	if (d > s)
		while (n--)
			*(d + n) = *(s + n);
	else
		ft_memcpy(dest, src, n);
	return (dest);
}
